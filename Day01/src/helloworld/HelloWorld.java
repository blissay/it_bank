package helloworld;
//클래스 이름은 무조건 첫 글자 대문자로 시작
//이어붙이는 단어의 첫 글자는 대문자 - 카멜 표기법
public class HelloWorld {

	//실행 메서드(함수) - 안에 적힌 내용을 실행
	//main이 없는 클래스는 사용은 가능하지만 실행되지 않음
	public static void main(String[] args) {
		//sysout + (ctrl+space) - 자동완성 가능
		System.out.println("Hello World!");
		System.out.println();

	}

}
